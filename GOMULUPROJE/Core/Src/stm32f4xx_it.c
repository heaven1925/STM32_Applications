/**
  * @brief This function handles EXTI line[9:5] interrupts.
  */
void EXTI9_5_IRQHandler(void)
{
	/* USER CODE BEGIN EXTI9_5_IRQn 0 */
	if(__HAL_GPIO_EXTI_GET_FLAG(GPIO_PIN_5))	// Kesme PIN5'ten geldiyse,
	{
		if(sdInt==1)	// SD Kart butonuna basıldı mı?
			sdInt=0;	// Basıldıysa SD Off
		else
			sdInt=1;	// Basılmadıysa SD ON
		__HAL_GPIO_EXTI_CLEAR_FLAG(GPIO_PIN_5);	// Kesme bayrağını temizler.
	}
	if(__HAL_GPIO_EXTI_GET_FLAG(GPIO_PIN_6))	// KESME Pin6'dan geldiyse,
	{
		if(wifiInt==1)  // Wifi butonuna basıldı mı?
			wifiInt=0;	// Basıldıysa wifi Off
		else
			wifiInt=1; // Basılmadıysa wifi ON
		__HAL_GPIO_EXTI_CLEAR_FLAG(GPIO_PIN_6); // Kesme bayrağını temizler.
	}
	if(__HAL_GPIO_EXTI_GET_FLAG(GPIO_PIN_8)) // KESME Pin8'den geldiyse,
	{

		if(fanInt==1) // Fan butonuna basıldı mı?
			fanInt=0; // Basıldıysa fan Off
		else
			fanInt=1;	// Basılmadıysa fan ON
		__HAL_GPIO_EXTI_CLEAR_FLAG(GPIO_PIN_8);  // Kesme bayrağını temizler.
	}
	/* USER CODE END EXTI9_5_IRQn 0 */
	HAL_GPIO_EXTI_IRQHandler(GPIO_PIN_5);
	HAL_GPIO_EXTI_IRQHandler(GPIO_PIN_6);
	HAL_GPIO_EXTI_IRQHandler(GPIO_PIN_8);
	/* USER CODE BEGIN EXTI9_5_IRQn 1 */
	/* USER CODE END EXTI9_5_IRQn 1 */
}

